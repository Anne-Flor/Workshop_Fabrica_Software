#2 - Crie um programa que leia dois números e exiba a soma dos dois número, a subtração, 
#multilplicação e divisão. 
print("Bem vindo a calculadora")
n1 = float(input("Digite o primeiro número : "))
n2 = float(input("Digite o segundo número : "))
soma = n1 + n2 
sub  = n1 - n1 
mul  = n1 * n2 
div  = n1 / n2
print(f"O resultado da soma entre {n1} e {n2}, é {soma}")
print(f"O resultado da subtração entre {n1} e {n2} é {sub}")
print(f"O resultado da multiplicação entre {n1} e {n2} é {mul}")
print(f"O resultado da divisão entre {n1} e {n2} é {div}")


